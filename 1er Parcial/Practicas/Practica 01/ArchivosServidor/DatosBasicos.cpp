//Tipos de datos basicos
#include <iostream>

using namespace std;

int main (void) {
    int numero = 15; //int -> numero entero
    float flotante = 10.45; // float -> numero con decimales
    double mayor = 16.3456;// double -> numero con decimales extensos
    char letra = 'h'; // char -> caracter, se asigna con comillas simples (1 byte)
    cout << numero << endl;
    cout << flotante << endl;
    cout << mayor << endl;
    cout << letra << endl;
    return 0;
}